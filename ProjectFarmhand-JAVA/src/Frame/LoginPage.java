    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
        String username = usernameTF.getText();
        String password = new String(passwordPF.getPassword());
        User user = ur.getUser(username,password);
        if (user != null) {
            String role = user.getRole();
            if (role.equals("user")) {
                ShoppingPage frame = new ShoppingPage(user);
                frame.setVisible(true);
                this.setVisible(false);
                this.dispose();
            } else if (role.equals("agent")) {
                AgentDashBoard frame = new AgentDashBoard(user);
                frame.setVisible(true);
                this.setVisible(false);
                this.dispose();

            } else if (role.equals("admin")) {

            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Invalid username and/or password", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_loginBtnActionPerformed

